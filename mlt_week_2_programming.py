import numpy as np

n_samples = X.shape[0]

# Calculate the polynomial kernel matrix with degree 2
def polynomial_kernel(X1, X2, degree=2, c=1):
    return (np.dot(X1, X2.T) + c) ** degree

K_poly = polynomial_kernel(X, X, degree=2, c=1)

# Calculate the conversion quantity
conversion_quantity = (1 / n_samples) * np.sqrt(np.sum(K_poly**2))

print(f"The polynomial kernel matrix K (degree 2) has been calculated.")
