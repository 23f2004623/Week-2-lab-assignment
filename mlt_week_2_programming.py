"""## Problem 7
Find the projections (scalar) of all the data points on to the first two principal components obtained using polynomial kernel of degree 2.

<u>Conversion</u>

if $\alpha_1, \alpha_2, ..., \alpha_n$ are the prjections on the first PC and $\beta_1, \beta_2, ..., \beta_n$ are the projections on the second PC, enter the following quantity as your answer correct to two decimal places:

$$\sum_{i = 1}^{n} \alpha_i + \sum_{i = 1}^{n} \beta_i$$
"""

import numpy as np

# Perform eigen-decomposition on the centered kernel matrix KC
eigenvalues, eigenvectors = np.linalg.eigh(KC)

# Sort eigenvalues and eigenvectors in descending order
sorted_indices = np.argsort(eigenvalues)[::-1]
eigenvalues = eigenvalues[sorted_indices]
eigenvectors = eigenvectors[:, sorted_indices]

# Select the first two principal components (eigenvectors)
# Ensure eigenvalues are positive before taking sqrt
# Handle potential small negative eigenvalues due to numerical precision by setting them to 0

top_eigenvalues = eigenvalues[:2]
top_eigenvectors = eigenvectors[:, :2]

# Normalize the eigenvectors to get the principal components (alpha vectors)
# Only consider positive eigenvalues for normalization

alphas = np.zeros_like(top_eigenvectors)
for i in range(2):
    if top_eigenvalues[i] > 1e-9:  # Check if eigenvalue is significantly positive
        alphas[:, i] = top_eigenvectors[:, i] / np.sqrt(top_eigenvalues[i])
    else:
        # If eigenvalue is zero or negative, set corresponding alpha to zero
        alphas[:, i] = np.zeros_like(top_eigenvectors[:, i])

# Calculate the projections of the data points onto the first two principal components
# Projections are KC @ alpha (where alpha are the normalized eigenvectors)

# First principal component projections (alpha_1)
projections_pc1 = np.dot(KC, alphas[:, 0])

# Second principal component projections (alpha_2)
projections_pc2 = np.dot(KC, alphas[:, 1])

# Calculate the conversion quantity: sum of projections on PC1 + sum of projections on PC2
conversion_quantity_projections = np.sum(projections_pc1) + np.sum(projections_pc2)

print(f"Sum of projections on the first principal component: {np.sum(projections_pc1):.3f}")
print(f"Sum of projections on the second principal component: {np.sum(projections_pc2):.3f}")
print(f"Conversion quantity (sum of all projections): {conversion_quantity_projections:.2f}")

